def generate_excel(path: Path, common_info, fields_info, test_cases):
    wb = Workbook()
    fill = PatternFill("solid", fgColor="D9E1F2")
    thin = Side(style="thin", color="A6A6A6")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)

    ws1 = wb.active
    ws1.title = "Table1"
    ws1["A1"] = "Таблица 1. Общая информация о тестировании"
    ws1["A1"].font = Font(bold=True, size=13)
    ws1.merge_cells("A1:B1")
    ws1["A2"] = "Индивидуальное задание:"
    ws1["B2"] = 'Практическая работа "Диаграмма вариантов использования" (по проекту Журнал МПТ)'
    ws1["A2"].font = Font(bold=True)
    for c in ("A2", "B2"):
        ws1[c].border = border
        ws1[c].alignment = Alignment(wrap_text=True, vertical="top")
    row = 4
    for k, v in common_info.items():
        ws1.cell(row, 1, k)
        ws1.cell(row, 2, v)
        ws1.cell(row, 1).font = Font(bold=True)
        ws1.cell(row, 1).fill = fill
        ws1.cell(row, 1).border = border
        ws1.cell(row, 2).border = border
        ws1.cell(row, 1).alignment = Alignment(wrap_text=True, vertical="top")
        ws1.cell(row, 2).alignment = Alignment(wrap_text=True, vertical="top")
        row += 1
    ws1.column_dimensions["A"].width = 40
    ws1.column_dimensions["B"].width = 95

    ws2 = wb.create_sheet("Table2")
    ws2["A1"] = "Таблица 2. Описание информационных полей для тестирования"
    ws2["A1"].font = Font(bold=True, size=13)
    ws2.merge_cells("A1:B1")
    ws2["A3"] = "Наименование"
    ws2["B3"] = "Описание"
    for c in ("A3", "B3"):
        ws2[c].font = Font(bold=True)
        ws2[c].fill = fill
        ws2[c].border = border
        ws2[c].alignment = Alignment(horizontal="center")
    r = 4
    for name, desc in fields_info:
        ws2.cell(r, 1, name)
        ws2.cell(r, 2, desc)
        ws2.cell(r, 1).border = border
        ws2.cell(r, 2).border = border
        ws2.cell(r, 1).alignment = Alignment(wrap_text=True, vertical="top")
        ws2.cell(r, 2).alignment = Alignment(wrap_text=True, vertical="top")
        r += 1
    ws2.column_dimensions["A"].width = 42
    ws2.column_dimensions["B"].width = 95

    ws3 = wb.create_sheet("Table3")
    ws3["A1"] = "Таблица 3. Test Case (группа действующих лиц: Преподаватель)"
    ws3["A1"].font = Font(bold=True, size=13)
    ws3.merge_cells("A1:M1")
    headers = [
        "№",
        "Test Case ID",
        "Приоритет",
        "Название тестирования",
        "Резюме испытания",
        "Шаги тестирования",
        "Данные тестирования",
        "Ожидаемый результат",
        "Фактический результат",
        "Предпосылки",
        "Постусловия",
        "Статус",
        "Комментарии",
    ]
    for i, h in enumerate(headers, 1):
        c = ws3.cell(3, i, h)
        c.font = Font(bold=True)
        c.fill = fill
        c.border = border
        c.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
    for idx, tc in enumerate(test_cases, 1):
        rr = 3 + idx
        vals = [idx, *tc]
        for cc, val in enumerate(vals, 1):
            cell = ws3.cell(rr, cc, val)
            cell.border = border
            cell.alignment = Alignment(wrap_text=True, vertical="top")
    for col, width in zip("ABCDEFGHIJKLM", [5, 22, 12, 28, 32, 50, 28, 34, 30, 30, 26, 10, 24]):
        ws3.column_dimensions[col].width = width

    wb.save(path)


