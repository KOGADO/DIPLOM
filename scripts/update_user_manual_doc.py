def find_source() -> Path:
    matches = [p for p in SOURCE_DIR.iterdir() if p.suffix.lower() == ".docx" and p.stat().st_size == 32580915]
    if not matches:
        raise FileNotFoundError("Не найден исходный файл руководства пользователя")
    return matches[0]


def clear_document(document: Document) -> None:
    body = document.element.body
    for child in list(body):
        if child.tag.endswith("sectPr"):
            continue
        body.remove(child)


def add_paragraph(document: Document, text: str = "", bold: bool = False, center: bool = False) -> None:
    paragraph = document.add_paragraph()
    run = paragraph.add_run(text)
    run.bold = bold
    run.font.size = Pt(12)
    if center:
        paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER


def add_title(document: Document, text: str) -> None:
    add_paragraph(document, text, bold=True, center=True)


def add_section(document: Document, text: str) -> None:
    add_paragraph(document, text, bold=True)


