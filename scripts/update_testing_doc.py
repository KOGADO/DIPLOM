def find_source() -> Path:
    marker = "Образцова"
    matches = [
        path
        for path in DOWNLOADS.glob("*.docx")
        if marker in path.name and "Сценарий" in path.name and not path.name.startswith("~$")
    ]
    if not matches:
        raise FileNotFoundError("Не найден исходный документ в Downloads")
    return matches[0]


def set_paragraph(paragraph, text: str) -> None:
    for run in paragraph.runs:
        run.text = ""
    if paragraph.runs:
        paragraph.runs[0].text = text
    else:
        paragraph.add_run(text)


def clear_cell(cell) -> None:
    for paragraph in cell.paragraphs:
        for run in paragraph.runs:
            run.text = ""
        paragraph.text = ""


def set_cell(cell, text: str) -> None:
    clear_cell(cell)
    cell.text = text


def set_table(table, rows: list[list[str]]) -> None:
    while len(table.rows) < len(rows):
        table.add_row()
    while len(table.rows) > len(rows):
        row = table.rows[-1]
        row._tr.getparent().remove(row._tr)

    for row_idx, row_data in enumerate(rows):
        for col_idx, value in enumerate(row_data):
            set_cell(table.rows[row_idx].cells[col_idx], value)


