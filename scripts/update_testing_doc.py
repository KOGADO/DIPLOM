    def box(x, y, w, h, text, fnt=small):
        draw.rectangle((x, y, x + w, y + h), outline="black", width=2, fill="white")
        lines = text.split("\n")
        line_h = 21
        total = len(lines) * line_h
        cy = y + (h - total) / 2
        for line in lines:
            bbox = draw.textbbox((0, 0), line, font=fnt)
            tx = x + (w - (bbox[2] - bbox[0])) / 2
            draw.text((tx, cy), line, fill="black", font=fnt)
