def find_source() -> Path:
    matches = [path for path in SOURCE_DIR.iterdir() if path.suffix.lower() == ".docx" and path.stat().st_size == 39777]
    if not matches:
        raise FileNotFoundError("Не найден исходный файл приложения Ж")
    return matches[0]


def set_paragraph_text(paragraph, text: str) -> None:
    for run in paragraph.runs:
        run.text = ""
    if paragraph.runs:
        paragraph.runs[0].text = text
    else:
        paragraph.add_run(text)


def set_table(table, rows: list[list[str]]) -> None:
    while len(table.rows) < len(rows):
        table.add_row()
    while len(table.rows) > len(rows):
        row = table.rows[-1]
        row._tr.getparent().remove(row._tr)
    for row_index, row_data in enumerate(rows):
        for col_index, value in enumerate(row_data):
            table.rows[row_index].cells[col_index].text = value


def remove_table(table) -> None:
    element = table._element
    element.getparent().remove(element)


def insert_clean_table_after(document: Document, paragraph_index: int, rows: list[list[str]], style_name) -> None:
    table = document.add_table(rows=len(rows), cols=len(rows[0]))
    if style_name:
        table.style = style_name
    for row_index, row_data in enumerate(rows):
        for col_index, value in enumerate(row_data):
            table.rows[row_index].cells[col_index].text = value
    document.paragraphs[paragraph_index]._p.addnext(table._tbl)


def main() -> None:
    document = Document(find_source())
    replacements = {
        0: "ПРИЛОЖЕНИЕ Ж. ВХОДНЫЕ И ВЫХОДНЫЕ ДАННЫЕ",
        2: "В данном программном документе приведены входные и выходные данные WEB-приложения «Журнал МПТ».",
        6: "В таблице 1 представлены входные данные, вводимые пользователем при использовании WEB-приложения «Журнал МПТ».",
        9: "В таблице 2 представлены выходные данные, которые имеются в WEB-приложении «Журнал МПТ».",
    }
    for index, text in replacements.items():
        if index < len(document.paragraphs):
            set_paragraph_text(document.paragraphs[index], text)

    styles = [table.style for table in document.tables]
    for table in list(document.tables):
        remove_table(table)

    insert_clean_table_after(document, 7, INPUT_ROWS, styles[0] if styles else None)
    insert_clean_table_after(document, 10, OUTPUT_ROWS, styles[1] if len(styles) > 1 else (styles[0] if styles else None))

    document.save(OUTPUT)
    print(OUTPUT)


if __name__ == "__main__":
    main()
