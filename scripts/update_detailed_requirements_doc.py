def find_source() -> Path:
    matches = [
        path
        for path in SOURCE_DIR.iterdir()
        if path.suffix.lower() == ".docx"
        and "1_19" in path.name
        and "Приложение_Д" in path.name
        and "Подробные_требования" in path.name
    ]
    if not matches:
        raise FileNotFoundError("Не найден исходный файл приложения Д")
    return matches[0]


def set_paragraph_text(paragraph, text: str) -> None:
    for run in paragraph.runs:
        run.text = ""
    if paragraph.runs:
        paragraph.runs[0].text = text
    else:
        paragraph.add_run(text)


def remove_paragraph(paragraph) -> None:
    element = paragraph._element
    element.getparent().remove(element)


def main() -> None:
    document = Document(find_source())

    while len(document.paragraphs) < len(TEXTS):
        document.add_paragraph()

    for index, text in enumerate(TEXTS):
        set_paragraph_text(document.paragraphs[index], text)

    for paragraph in list(document.paragraphs[len(TEXTS) :]):
        remove_paragraph(paragraph)

    document.save(OUTPUT)
    print(OUTPUT)


if __name__ == "__main__":
    main()
