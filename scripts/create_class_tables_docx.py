def set_cell_text(cell, text, bold=False):
    cell.text = ""
    paragraph = cell.paragraphs[0]
    run = paragraph.add_run(text)
    run.bold = bold
    run.font.name = "Times New Roman"
    run.font.size = Pt(12)
    paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER if bold else WD_ALIGN_PARAGRAPH.LEFT
    cell.vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.CENTER


def main():
    document = Document()
    section = document.sections[0]
    section.left_margin = Cm(2)
    section.right_margin = Cm(1.5)
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)

    for index, (title, rows) in enumerate(TABLES):
        if index:
            document.add_paragraph()

        title_paragraph = document.add_paragraph(title)
        title_paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
        title_run = title_paragraph.runs[0]
        title_run.font.name = "Times New Roman"
        title_run.font.size = Pt(12)

        table = document.add_table(rows=len(rows), cols=3)
        table.alignment = WD_TABLE_ALIGNMENT.CENTER
        table.style = "Table Grid"

        for row_index, row_data in enumerate(rows):
            for col_index, value in enumerate(row_data):
                is_header = row_index in (0, 1)
                set_cell_text(table.rows[row_index].cells[col_index], value, bold=is_header)

        for row in table.rows:
            row.cells[0].width = Cm(1.2)
            row.cells[1].width = Cm(5.3)
            row.cells[2].width = Cm(10.5)

    document.save("class_diagram_tables.docx")


if __name__ == "__main__":
    main()
